#include <unistd.h>

void	tpel(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	int a;
	int b;

	a = -1;
	while (++a < 99)
	{
		b = a;
		while (++b <= 99)
		{
			tpel(a / 10 + '0');
			tpel(a % 10 + '0');
			tpel(' ');
			tpel(b / 10 + '0');
			tpel(b % 10 + '0');
			if (a != 98)
			{
				tpel(',');
				tpel(' ');
			}
		}
	}
}
