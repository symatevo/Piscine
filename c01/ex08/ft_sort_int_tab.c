void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int j;
	int srt;

	i = 0;
	j = 0;
	while (i < size)
	{
		j = i + 1;
		while (j < size)
		{
			if (tab[i] > tab[j])
			{
				srt = tab[i];
				tab[i] = tab[j];
				tab[j] = srt;
			}
			j++;
		}
		i++;
	}
}
